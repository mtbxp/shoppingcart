const dadItems = document.getElementsByClassName('items')[0];

const createProductImageElement = (imageSource) => {
  const img = document.createElement('img');
  img.className = 'item__image';
  img.src = imageSource;
  return img;
};

const createCustomElement = (element, className, innerText) => {
  const e = document.createElement(element);
  e.className = className;
  e.innerText = innerText;
  return e;
};

const createProductItemElement = ({ id: sku, title: name, thumbnail: image }) => {
  const section = document.createElement('section');
  section.className = 'item';

  section.appendChild(createCustomElement('span', 'item__sku', sku));
  section.appendChild(createCustomElement('span', 'item__title', name));
  section.appendChild(createProductImageElement(image));
  section.appendChild(createCustomElement('button', 'item__add', 'Adicionar ao carrinho!'));

  return section;
};

const callElements = () => { // const arrow function criada para chamar e inserir os elementos da api na tela ->
  fetchProducts('computador').then((e) => // chamando a função da api e em caso de sucesso acessar elemento ->
    e.results.forEach((element) => // acessando o objeto results, forEach para percorrer cada elemento do results ->
    dadItems.appendChild(createProductItemElement(element)))); // acessando items pai e adicionando filho a ele pela função creat... e percorrendo os elementos que estão trabalhando
};

